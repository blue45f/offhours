export function toCard(s: SeedSpace): SpaceCard {
  const { detail: _detail, ...card } = s
  return card
}

export function toDetail(s: SeedSpace): SpaceDetail {
  const { detail, ...card } = s
  return { ...card, ...detail }
}

/** /spaces/slug/:slug/gallery — 호스트가 올린 실제 이용 사진(용도별). */
export function galleryFor(s: SeedSpace): GalleryPhoto[] {
  const purposes = ['PARTY', 'WEDDING', 'MEETING', 'POPUP', 'SHOOT', 'OTHER'] as const
  return s.detail.photos.map((p, i) => ({
    url: p.url,
    purpose: purposes[i % purposes.length],
    completedAt: null,
  }))
}
